
public class q8 {

	public static void main(String[] args) {
			int x,y;
			x=1;
			y=2;
			System.out.println("Before swapping:");
			System.out.println(x);
			System.out.println(y);
			x=x+y;
			y=x-y;
			x=x-y;
			System.out.println("After swapping:");
			System.out.println(x);
			System.out.println(y);
		
	}

}
